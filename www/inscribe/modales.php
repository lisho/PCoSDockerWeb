            <div>
              <p class="text-justify justificado">
                La implantación de las TICs y otras tecnologías en la vida diaria de las personas y su incorporación a las actividades más esenciales y cotidianas 
                (relacionarnos, realizar trámites, estar informados, obtener recursos, formarnos, acceder al empleo…) ha dado lugar al fenómeno denominado “brecha digital”, 
                entendido como la distancia que se genera entre las personas que utilizan estas tecnologías en su vida diaria y aquellas otras que no tienen acceso a ellas 
                o que no saben utilizarlas. Cuando este factor se combina con otros que operan sobre las situaciones de vulnerabilidad social, las situaciones de desventaja 
                se potencian y la exclusión social se agrava.
              </p>
              <p class="text-justify justificado">
                Cada vez más, la <b>e-inclusión</b>, entendida como la <b>posibilidad de <span class="rojo">acceso y el grado de utilización de las nuevas tecnologías</span>
                de la información y la comunicación (TICs)</b>, se convierte en un factor añadido de riesgo/protección ante la exclusión social, 
                afectando a otros aspectos de la vida de las personas como la formación, el acceso al empleo, 
                las relaciones con los demás o la plena participación social (acceso a las administraciones, entidades bancarias, 
                comprensión crítica de la información...). Y este factor se torna aún más importante en la situación de pandemia mundial en la que nos encontramos.
              </p>
              <p class="text-justify justificado">
                En este contexto el Ayuntamiento de León pone en marcha el proyecto de <b>"Apoyo para el acceso a la administración electrónica y reducción de la brecha 
                digital en la población más vulnerable de León"</b> para <i>poner en marcha medidas orientadas a corregir los problemas de <b class="rojo">acceso 
                a la administración electrónica</b> de la población con mayores dificultades, ofrecer <b class="rojo">respuestas adecuadas a las situaciones de e-exclusión</b> y 
                proporcionar <b class="rojo"><u>servicios de acompañamiento digital</u></b>, facilitando una atención más eficaz, más eficiente y de mayor calidad a la población que padece estas situaciones.
                </i>
              </p>
